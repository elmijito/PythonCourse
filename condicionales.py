#condicionales
# 1. Pedimos un número (Devuelve un input)
# 2. Convertimos el float para aceptar condicionales
# 3. Comparamos con 0 y mostramos resultado

numero = float(input("Introduce un número: "))
if numero > 0:
    print("Es positivo") # caso > a 0)
elif numero < 0:
    print ("Es negativo") #caso < a 0)
else:
    print("es cero") # caso == 0)
# Fin del programa

# Igual a == b
# Diferente a != b
# Mayor que > b
# Menor que < b
# Mayor o igual que >= b
# Menor o igual que <= b

